#include "libft.h"

int		ft_power(int n, int p)
{
	int		i;
	int		nb;

	if (p == 0)
		return (1);
	i = 0;
	nb = 1;
	while (i < p)
	{
		nb = nb * n;
		i++;
	}
	return (nb);
}
